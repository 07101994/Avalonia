





namespace Perspex.Controls
{
    /// <summary>
    /// Controls the wrapping mode in a <see cref="TextBlock"/>.
    /// </summary>
    public enum TextWrapping
    {
        /// <summary>
        /// Text should not wrap.
        /// </summary>
        NoWrap,

        /// <summary>
        /// Text can wrap.
        /// </summary>
        Wrap
    }
}